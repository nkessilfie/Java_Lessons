package java_basics;

public class Variables {

	public static void main(String[] args) {
		int x; // variable declaration
		x = 23; // variable assignment
		System.out.println(x);
		
		String words; // variables start with letters, not numbers or symbols
		words = "this is a sentence";
		System.out.println(words);
		
		int y = 54; //declaration and assignment
		//certain data types support operators
		System.out.println(y + 40);
		
		// Plus sign concatenates Strings
		System.out.println(words + " and these are some more words");
	}

}
